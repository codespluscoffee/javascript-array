
//Declare Array
const colors = ["red", "orange", "purple"];

// Convert Array to String
const colors = ["red", "orange", "purple"];
console.log(colors.toString());

//result "red,orange,purple"

// Join Array
const colors = ["red", "orange", "purple"];
console.log(colors.join(" * "));

//result "red * orange * purple"

// Popping
const colors = ["red", "orange", "purple"];
let x = colors.pop();

//result ["red", "orange"]

//Pushing 
const colors = ["red", "orange", "purple"];
colors.push("green");

//result ["red", "orange", "purple", "green"]

// Shifting Elements
const colors = ["red", "orange", "purple"];
let x = colors.shift();    // x = "red" 


//Unshift Elements
const colors = ["red", "orange", "purple"];
colors.unshift("green");   //["green","red", "orange", "purple"]




